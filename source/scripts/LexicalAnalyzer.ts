		public checkForAcceptState(state: number): boolean {

			switch (state) {
				case 1:
					console.log("$ Found");
					this.tokenArray.push(new Token("EOF", "$"));
					Main.createNewUpdateMessage("$ Token Found on line " + this.lineNumber);
					break;
				case 2:
					console.log("{ Found");
					this.tokenArray.push(new Token("Left Bracket", "{"));
					Main.createNewUpdateMessage("{ Token Found on line " + this.lineNumber);
					break;
				case 3:
					console.log("} Found");
					this.tokenArray.push(new Token("Right Backet", "}"));
					Main.createNewUpdateMessage("} Token Found on line " + this.lineNumber);
					break;
				case 4:
					console.log("( Found");
					this.tokenArray.push(new Token("Left Para", "("));
					Main.createNewUpdateMessage("( Token Found on line " + this.lineNumber);
					break;
				case 5:
					console.log(") Found");
					this.tokenArray.push(new Token("Right Para", ")"));
					Main.createNewUpdateMessage(") Token Found on line " + this.lineNumber);
					break;
				case 6:
					console.log("! Found");
					this.tokenArray.push(new Token("EPoint" , "!"));
					Main.createNewUpdateMessage("! Token Found on line " + this.lineNumber);
					break;
				case 7:
					console.log("+ Found");
					this.tokenArray.push(new Token("plus-sign", "+"));
					Main.createNewUpdateMessage("+ Token Found on line " + this.lineNumber);
					break;
				case 8:
					console.log("= Found");
					this.tokenArray.push(new Token("Assignment", "="));
					Main.createNewUpdateMessage("= Token Found on line " + this.lineNumber);
					break;
				case 11:
					console.log("Type Found");
					this.tokenArray.push(new Token("type" , "int"));
					Main.createNewUpdateMessage("Type Token " + this.currentCharacters + " Found on line " + this.lineNumber);
					break;
				case 16:
					console.log("print keyword Found");
					this.tokenArray.push(new Token("keyword" , "print"));
					Main.createNewUpdateMessage("print Token Found on line " + this.lineNumber);
					break;
				case 17:
					console.log("Digit Found");
					this.tokenArray.push(new Token("digit" , this.currentCharacters));
					Main.createNewUpdateMessage("digit Token Found on line " + this.lineNumber);
					break;
				case 31:
					console.log("String Found");
					this.tokenArray.push(new Token("string" , this.currentCharacters));
					Main.createNewUpdateMessage("String  " + this.currentCharacters + " Token Found on line " + this.lineNumber);
					break;
				case 32:
					console.log("ID Found");
					this.tokenArray.push(new Token("Identifier" , this.currentCharacters.trim()));
					Main.createNewUpdateMessage("ID  " + this.currentCharacters.charAt(0) + " Token Found on line " + this.lineNumber);
					break;
				case 36:
					console.log("BoolVal Found");
					this.tokenArray.push(new Token("BoolVal", this.currentCharacters.trim()));
					Main.createNewUpdateMessage("BoolVal " + this.currentCharacters + " Token Found on line " + this.lineNumber);
					break;
				default:
					return false;
			}
			return true;
		}
		public generateTokens(sourceCode:string) {

			// Create variables

			var currentState: number = 0; // The current state
			var nextState: number;
			var nextCharacter: string;
			var insideString: number = 0;
			var nextTablePosition;
			var holder = "";
	
			for (var i = 0; i < sourceCode.length; i++) {

				// Check to see if the next character is a line number and if so then increase the counter and get next character
				if (sourceCode.charCodeAt(i) == 10) {	
					
					this.lineNumber++;
				}
				else {

				// Get the next character value
				nextCharacter = sourceCode.charAt(i);

				this.currentCharacters = this.currentCharacters + nextCharacter;

				console.log("Next Character: " + nextCharacter);

				// Get the table position of the character
				nextTablePosition = Utils.getCharacterPosition(nextCharacter);

				// Check to see if there is an error
				if(nextTablePosition == null) {
					Main.createNewErrorMessage("Unknown character " + this.currentCharacters + " found on line " + this.lineNumber);
					this.hasErrors = true;
					return;

				}

				console.log(nextTablePosition + " NTP");

				// Lookup in the matrix to find out where to go next
				nextState = _transitionTable[currentState][nextTablePosition];

				// Check the new state to see if it is an accepting state
				if (this.checkForAcceptState(nextState)) {
					currentState = 0;
					this.currentCharacters = "";
				}
				else {
					console.log("else if firing");
					currentState = nextState;
				}
			  }
			}
		}
